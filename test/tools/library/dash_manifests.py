    def set_encrypt(self, state=False):
        """
        Hide or expose any encryption keys.
        """
        cnt = 0

        for adpset in self.adp_crypt:
            for templ in adpset:
                tag = templ.tag

                if not tag.endswith("ContentProtection") and not tag.endswith(
                    "xContentProtectionx"
                ):
                    continue

                tag = tag[: -19 if tag.endswith("x") else -17]
                templ.tag = tag + (
                    "ContentProtection" if state else "xContentProtectionx"
                )
                cnt += 1

        return cnt

    @staticmethod
    def PT_parse_secs(field):
        """
        Convert a PT time duration into seconds
        """
        res = DASHManifest.PT_parse_regx.match(field)
        dur = 0.0

        for grp in res.groups():
            if grp is None:
                break
            if grp.endswith("H"):
                dur += float(grp[:-1]) * 3600.0
            elif grp.endswith("M"):
                dur += float(grp[:-1]) * 60.0
            else:
                dur += float(grp[:-1])

        return dur

    @staticmethod
    def PT_parse(field):
        """
        Convert a PT time duration into seconds
        """
        return timedelta(0, DASHManifest.PT_parse_secs(field))

    @staticmethod
    def PT_format(delta):
        secs = (
            delta.seconds + delta.microseconds / 1000000.0
            if type(delta) is timedelta
            else delta
        )

        if secs > 3600:
            return "PT%dH%dM%dS" % (
                secs // 3600,
                secs % 3600 // 60,
                round(secs % 60, 3),
            )
        if secs > 60:
            return "PT%dM%dS" % (secs % 3600 // 60, round(secs % 60, 3))
        return "PT%.4gS" % round(secs % 60, 3)


