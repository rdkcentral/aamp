class DASHManifest(Manifest):
    """
    Represent the contents of a DASH video and audio manifest and
    provides manipulation.
    """

    def __init__(self, path, content=None, rebase=None, default_start=None):
        super().__init__(path)

        self.orig_tree = ET.ElementTree()
        self.root = None
        self.base_url = None

        self.clear_indexes()
        self.prd_duration = {}  # Overall duration of each period
        self.prd_start = {}  # Start point in timeline of each period
        self.time_offset = 0
        self.overall_dur = 0
        self.remove_duplicate_req = {} # This is to handle 404 during All Profiles harvest.

        if content is None:
            with open(path, "rb") as f:
                content = f.read()

        # Pull all the namespace attributes out the incomming XML 'header' and
        # put them into the parser via register_namespace()
        # This is so that when the new XML is generated by the parser then
        # the namespace attributes will appear in the new XML
        # However if a namespace attribute is in the original XML but there are no
        # 'references' to it in the XML then ET will not put it into the XML header
        # hence a difference in 'header' may be seen

        ns_dict = dict(
            [node for _, node in ET.iterparse(io.BytesIO(content), events=["start-ns"])]
        )

        for key in ns_dict:
            ET.register_namespace(key, ns_dict[key])

        self.orig_tree = ET.parse(io.BytesIO(content))
        self.root = self.orig_tree.getroot()

        if "availabilityStartTime" in self.root.attrib:
            time_offset = self.date_parse(self.root.get("availabilityStartTime"))
            self.time_offset = int(time_offset.strftime("%s")) + 3600

        if "mediaPresentationDuration" in self.root.attrib:
            self.overall_dur = self.PT_parse_secs(
                self.root.get("mediaPresentationDuration")
            )
        for period in self.root:
            if period.tag.endswith("Period"):
                self.index_period(period)
            elif period.tag.endswith("BaseURL"):
                self.base_url = period

        self.orig_date = self.calc_range(default_start)

    def clear_indexes(self):
        """
        Clear down all indexes into the XML structure in before (re) building them
        """
        self.periods = []  # Period
        self.adpsets = []  # AdaptationSet
        self.urls = {}  # BaseURL

        self.segml = {}  # SegmentList
        self.templ = {}  # SegmentTemplate
        self.tlines = {}  # SegmentTimeline
        self.repts = {}  # Representation

        self.adp_crypt = set()
        self.band_info = {}
        self.bands = {}

        self.poll_details = {}

    def index_period(self, period):
        """
        Run through a period and index the AdaptationSet and the SegmentTimeline.
        We have to allow for the fact that the SegmentTemplate/SegmentTimeline can
        appear outside and under the Representation. Any AdaptationSets without a
        representation are removed.
        """
        self.periods.append(period)
        del_list = []

        # Process each AdaptationSet within the period and index.
        for adpset in period:
            if adpset.tag.endswith("AdaptationSet"):
                band_info = set()
                self.templ[adpset] = []
                mime_type = self.resolve_mime(adpset)

                # Index all of the SegmentTemplates and SegmentTimelines within an AdaptationSet
                for templ in adpset:
                    if templ.tag.endswith("ContentProtection"):
                        self.encrypted = True
                        self.adp_crypt.add(adpset)

                    elif self.index_template(adpset, templ, -1, ""):
                        pass

                    elif templ.tag.endswith("BaseURL"):
                        self.urls[adpset] = templ.text

                # Index each Representation within an AdaptationSet
                for rept in adpset:
                    if not rept.tag.endswith("Representation"):
                        continue

                    if mime_type is None:
                        mime_type = self.resolve_mime(rept)

                    rband = int(rept.get("bandwidth"))
                    rid = rept.get("id")
                    band_info.add((rband, rid))

                    self.repts.setdefault(adpset, []).append((rept, rband))
                    self.bands[rband] = [mime_type]

                    # Index each template within the Representation
                    for templ in rept:
                        if self.index_template(adpset, templ, rband, rid):
                            pass
                        elif templ.tag.endswith("BaseURL"):
                            self.urls[rept] = templ
                        elif templ.tag.endswith("SegmentList"):
                            self.segml.setdefault(adpset, []).append(
                                (rept, templ, rband, rid)
                            )
                        elif templ.tag.endswith("SegmentBase"):
                            self.segml.setdefault(adpset, []).append(
                                (rept, None, rband, rid)
                            )

                if not band_info:
                    del_list.append(adpset)
                    continue

                self.band_info[adpset] = band_info
                self.adpsets.append((period, adpset))

            elif adpset.tag.endswith("BaseURL"):
                self.urls[period] = adpset

        for adpset in del_list:
            del self.templ[adpset]
            if adpset in self.adp_crypt:
                self.adp_crypt.remove(adpset)

            period.remove(adpset)

    def index_template(self, adpset, templ, rband, rid):
        """
        Run through a SegmentTemplate and index the segments.
        """
        if not templ.tag.endswith("SegmentTemplate"):
            return False

        self.templ[adpset].append((templ, rband, rid))

        for tline in templ:
            if not tline.tag.endswith("SegmentTimeline"):
                continue
            self.tlines[templ] = tline
            break

        else:
            self.tlines[templ] = []

        return True

    def resolve_mime(self, obj):
        """
        Resolve the MIME type of an AdaptationSet or a AdaptationSet
        """
        mime_type = obj.get("contentType", None)
        if mime_type is not None:
            return mime_type.lower()

        mime_type = obj.get("mimeType", None)
        if mime_type is None:
            return None

        if "/" in mime_type:
            mime_type = mime_type.split("/", 1)[0]

        return mime_type.lower()

    def is_significant_time_diff(self,t1,t2):
        """
        True if two times differ by more that 1s
        """
        t3 = t1-t2
        return abs(t3.total_seconds())>1

    def calc_range(self, default_start):
        """
        Calculate the date/time range for the manifest together with the duration
        of each period.
        """
        if default_start is not None:
            start = default_start
            # self.root.set("publishTime", self.fmt_date(start))
        elif "publishTime" in self.root.attrib:
            start = self.date_parse(self.root.get("publishTime"))
        else:
            start = datetime.now()
            # self.root.set("publishTime", self.fmt_date(start))

        # do_rules = len(self.rule_list) <= 0
        self.first_date = end = start
        self.cur_period = None
        self.cur_offset = 0

        # Process each of the AdaptationSets and calculate their duration.
        for period, adpset in self.adpsets:
            if period is not self.cur_period and "duration" in period.attrib:
                dur = DASHManifest.PT_parse_secs(period.get("duration"))
                start, end = self.switch_period(period, end, round(dur, 1))

            adpset_ts = 1.0

            # Process each SegmentTemplate and calculate the duration
            for templ, rband, rid in self.templ[adpset]:

                start_num = int(templ.get("startNumber", -1))
                ts = float(templ.get("timescale", adpset_ts))
                if rband < 0:
                    adpset_ts = ts

                first_t = None
                next_t = 0

                # Calculate duration across all segments allowing for discontinuaties
                for seg in self.tlines[templ]:
                    t = int(seg.get("t", next_t))
                    r = int(seg.get("r", 0)) + 1
                    if first_t is None:
                        first_t = t

                    next_t = t + int(seg.get("d")) * r
                    start_num += r

                if first_t is not None:
                    dur = round((next_t - first_t) / ts, 1)
                elif period is self.cur_period:
                    continue

                if period is not self.cur_period:
                    if first_t is None:
                        dur = round(self.overall_dur - self.cur_offset, 1)

                        if dur <= 0:
                            dur = round(float(templ.get("duration", ts * 2)) / ts, 1)

                    start, end = self.switch_period(period, end, dur)

                elif self.is_significant_time_diff(end - start, timedelta(0, dur)):
                    mime_type = self.resolve_mime(adpset)
                    log.warning(
                        "Difference of duration between template AdaptationSets period=%s mime=%s band=%s %s %s",
                        period.get("id"),
                        mime_type,
                        rband,
                        end - start,
                        timedelta(0, dur),
                    )

            # Process each SegmentList and calculate the duration
            for rept, segml, rband, rid in self.segml.get(adpset, []):
                tot_dur = 0.0

                if segml is not None:
                    dur = int(segml.get("duration")) / int(segml.get("timescale"))

                    # Walk down SegmentURL within SegmentList
                    for segurl in segml:
                        if segurl.tag.endswith("SegmentURL"):
                            tot_dur += dur

                if period is not self.cur_period:
                    start, end = self.switch_period(period, end, tot_dur)

                elif self.is_significant_time_diff(end - start, timedelta(0, tot_dur)):
                    mime_type = self.resolve_mime(adpset)

                    log.warning(
                        "Difference of duration between template AdaptationSets period=%s mime=%s band=%s %d %d",
                        period.get("id"),
                        mime_type,
                        rband,
                        end - start,
                        timedelta(0, dur),
                    )

        if self.cur_period is not self.periods[-1]:
            start, end = self.switch_period(self.periods[-1], end, 0.0)

        self.last_date = end

        return self.first_date + timedelta(0)

    def switch_period(self, period, end, dur):
        """
        Handle setting of period duration and start index information when a period
        changes. This is only called for the first AdaptationSet within the Period.
        """
        self.cur_period = period

        if "start" in period.attrib:
            self.cur_offset = DASHManifest.PT_parse_secs(period.get("start"))

        self.prd_start[period] = timedelta(0, self.cur_offset)
        self.cur_offset += dur

        start = end
        end = end + timedelta(0, dur)
        self.prd_duration[period] = dur

        return start, end

    def switch_period(self, period, end, dur):
        """
        Handle setting of period duration and start index information when a period
        changes. This is only called for the first AdaptationSet.
        """
        self.cur_period = period

        if "start" in period.attrib:
            self.cur_offset = DASHManifest.PT_parse_secs(period.get("start"))

        self.prd_start[period] = timedelta(0, self.cur_offset)
        self.cur_offset += dur

        start = end
        end = end + timedelta(0, dur)
        self.prd_duration[period] = dur

        return start, end

    def write(self, path=None):
        """
        Write the manfest to a file.
        """
        if path is None:
            path = self.orig_path

        self.orig_tree.write(path)


    def reduce_bandwidth(self, band_list):
        """
        Reduce down the number of bandwidths supported by the top level manifest.
        """
        if type(band_list) in [str, int]:
            band_list = [int(band_list)]

        band_check = {int(band) for band in band_list if int(band) in self.bands}
        mime_upd = {self.bands[band][0] for band in band_check}

        log.info("Reducing to bandwidths %s for %s", band_check,mime_upd)
        if len(band_check) < len(band_list) or band_list == []:
            return False

        del_list = []
        ok = True
        adp_iter = iter(self.adpsets)
        next_period, next_adpset = next(adp_iter)

        # Process each period
        while next_period is not None:
            period = next_period
            matched = set()
            mime_rm = {}

            # Process each AdaptationSet within the Period
            while next_period is period:
                adpset = next_adpset
                mime_type = self.resolve_mime(adpset)

                if mime_type is None:
                    rept, rband = self.repts[adpset][0]
                    mime_type = self.resolve_mime(rept)

                if mime_type in mime_upd:
                    # Check each Representation for the AdaptationSet
                    for rept, rband in self.repts[adpset]:
                        if rband in band_check:
                            matched.add(mime_type)
                        else:
                            mime_rm.setdefault(mime_type, []).append(
                                (rband, adpset, rept)
                            )

                try:
                    next_period, next_adpset = next(adp_iter)
                except StopIteration:
                    next_period = None

            # Check whether we had a match to the MIME type
            for mime_type, rept_rm in mime_rm.items():
                if mime_type not in matched:
                    log.warning(
                        "Cannot remove %s bandwidths from period %s using %s",
                        mime_type,
                        period.get("id"),
                        [band[0] for band in rept_rm],
                    )
                    ok = False
                else:
                    del_list += rept_rm

        if not ok:
            return False

        for band, adpset, rept in del_list:
            adpset.remove(rept)

        self.clear_indexes()

        # Rebuild the indexes for each Period
        for period in self.root:
            if period.tag.endswith("Period"):
                self.index_period(period)

        return True


    def get_sub_files(self, abs_paths=False):
        """
        Return the list of audio and video URLs
        """
        res = set()

        for period, adpset in self.adpsets:
            flist = self.parse_template(adpset)

            for fn, templ, rband, rid, do_number in flist:
                res.add(self.abs_path(fn) if abs_paths else fn)

        return res

    def __str__(self):
        """
        Convert the whole of the manifest to a string.
        """
        b_string = ET.tostring(self.root, encoding="UTF-8", xml_declaration=True)
        return str(b_string.decode()) + "\n"

    def get_seg_list(
        self, from_seg=None, abs_paths=False, seg_list=None, rband_filt=None
    ):
        """
        Returns the file names associated with segments defined in the manifest.
        """
        if from_seg is None:
            from_seg = {}
        if seg_list is None:
            seg_list = SegmentList()

        # Process each of the AdaptationSets
        for period, adpset in self.adpsets:
            prefix = self.urls[period].text if period in self.urls else ""
            if prefix == "":
		# RDKAAMP-1833
                prefix = ""
                if self.base_url is not None:
                    prefix = self.base_url.text
                else:
                    for adp in adpset:
                        if adp.tag.endswith("BaseURL"):
                            self.base_url = adp
                            self.urls[adpset] = adp.text 
                            prefix = self.base_url.text

            encrypted = adpset in self.adp_crypt
            adpset_ts = 1
            adpset_dur = 1

            adpset_id = adpset.get("id")
            period_id = period.get("id")
            templ_lst = self.templ[adpset]

            # Obtain any AdaptationSet values from the default template
            if len(templ_lst) > 0:
                templ, rband, rid = templ_lst[0]

                if rband < 0:
                    adpset_ts = float(templ.get("timescale", 1))
                    adpset_dur = float(templ.get("duration", 1))

            pts = 0.0 # RDKAAMP-1834
            # Run through each of the Representations for this AdaptationSet with templates
            for fn_templ, templ, rband, rid, do_number in self.parse_template(
                adpset, field="media"
            ):
                if rband_filt is not None and rband not in rband_filt:
                    continue

                key = "P%s_A%s_R%s" % (period_id, adpset_id, rid)
                seg_no = from_seg.get(key, -1)
                if abs_paths:
                    fn_templ = self.abs_path(fn_templ, self.urls.get(adpset, prefix))

                tlines = self.tlines[templ] # RDKAAMP-1834
                    
                # Handle first time for this template within a Represetation
                if seg_no < 0:
                    attrs = AttribList(adpset.attrib)
                    setattr(attrs,"period_id",period_id)
                    if pts == 0.0:
                        pts = float(tlines[0].get('t', 0))
                        setattr(attrs,"pts", pts )
                    for rept, chek_rband in self.repts[adpset]:
                        if rband != chek_rband:
                            continue

                        attrs.merge(rept.attrib)
                        break

                    seg_list.new_file(key, encrypted, attrs)
                    fn = templ.get("initialization")

                    # Handle template with an initialisation file
                    if fn is not None:
                        if abs_paths:
                            fn = self.abs_path(fn, self.urls.get(adpset, prefix))
                        seg_list.add_init(self.template_subs(fn, rband, rid)[0], key)

                tlines = self.tlines[templ]
                start_num = int(templ.get("startNumber", -1))
                ts = float(templ.get("timescale", adpset_ts))

                # No segment list and file generation based upon $Number$
                if len(tlines) <= 0:
                    dur = self.prd_duration[period]
                    intv = float(templ.get("duration", adpset_dur)) / ts

                    # Manifest that does not change and relies upon current time to calculate $Number$ for segment name
                    if start_num <= 0:
                        now = time.time()
                        start_num = int((now - self.time_offset) / intv) - 1
                        end_num = start_num + int(dur / intv) + 1

                        if templ not in self.poll_details:
                            self.poll_interval = intv

                            self.poll_details[templ] = start_num

                    # Segment name uses $Number$ based upon relative time and startNumber attribute
                    else:
                        end_num = start_num + int(dur / intv)

                    if end_num <= seg_no: print("Continue - Pass.")

                    # Generate the names of the files from increasing segement value using $Number$
                    for n in range(
                        start_num if seg_no < start_num else seg_no, end_num
                    ):
                        seg_list.add_file(fn_templ % n, intv, key)

                    seg_list.add_file(fn_templ % end_num, dur % intv, key)
                    seg_no = end_num

                # Segment list and file generation based upon $Number$
                elif do_number:
                    # Run through the segments in the AdaptationSet
                    for seg in tlines:
                        end_num = start_num + int(seg.get("r", 0)) + 1
                        if end_num <= seg_no: print("Continue - Pass.")

                        intv = float(seg.get("d")) / ts

                        # Generate the names of the files from increasing segement value using $Number$
                        for num in range(
                            start_num if seg_no < start_num else seg_no, end_num
                        ):
                            if end_num <= seg_no:
                                temp_file_name = fn_templ.split("/")[-1].replace("%d", str(num)) 

                                if temp_file_name not in self.remove_duplicate_req.keys(): 
                                    seg_list.add_file(fn_templ % num, intv, key) 
                                    self.remove_duplicate_req[temp_file_name] = str(1) 
                                    
                                else: 
                                    self.remove_duplicate_req[temp_file_name] = str(int(self.remove_duplicate_req[temp_file_name]) + 1) 
                                    
                            else:
                                seg_list.add_file(fn_templ % num, intv, key) 
                                                                                            
                        seg_no = start_num = end_num

                # Segmentlist and filename generation based upon $Time$
                else:
                    next_t = 0
                    # Run through the segments in the AdaptationSet
                    for seg in tlines:
                        t = int(seg.get("t", next_t))
                        r = int(seg.get("r", 0))
                        d = int(seg.get("d"))

                        next_t = t + d * (r + 1)
                        if next_t <= seg_no: print("Continue - Pass.")

                        intv = float(d) / ts

                        # Generate the names of the files from the increasing 't' value using $Time$
                        for t in range(t if seg_no < t else seg_no, next_t, d):
                            seg_list.add_file(fn_templ % t, intv, key)
                        seg_no = next_t

                from_seg[key] = seg_no

            # Run through each of the Representations for this AdaptationSet without templates
            for rept, segml, rband, rid in self.segml.get(adpset, []):
                if rband_filt is not None and rband not in rband_filt:
                    continue

                key = "P%s_A%s_R%s" % (period_id, adpset_id, rid)
                seg_no = from_seg.get(key, -1)

                # Representation contains SegmentList
                if segml is not None:
                    segml_prefix = (
                        self.urls[rept].text
                        if rept in self.urls
                        else self.urls.get(adpset, prefix)
                    )

                    # Handle first time for SegmentList within a Represetation
                    if seg_no < 0:
                        fn = None

                        # Search for initialisation
                        for segurl in segml:
                            if segurl.tag.endswith("Initialization"):
                                fn = segurl.get("sourceURL")
                                break
                        attrs=AttribList(adpset.attrib).merge(rept.attrib)
                        setattr(attrs,"period_id",period_id)
                        seg_list.new_file(
                            key,
                            encrypted,
                            attrs,
                        )

                        # Handle segmentlist with an initialisation file
                        if fn is not None:
                            if abs_paths:
                                fn = self.abs_path(fn, segml_prefix)
                            seg_list.add_init(fn, key)

                    idx = -1
                    dur = int(segml.get("duration")) / int(segml.get("timescale"))

                    # Walk down SegmentURL within SegmentList
                    for segurl in segml:
                        if not segurl.tag.endswith("SegmentURL"):
                            continue

                        idx += 1
                        if idx <= seg_no: print("Continue - Pass.")
                        fn = segurl.get("media")
                        if abs_paths:
                            fn = self.abs_path(fn, segml_prefix)

                        seg_list.add_file(fn, dur, key)
                    from_seg[key] = idx

                # Representation contains SegmentBase
                elif rept in self.urls and seg_no < 0:
                    path = self.urls[rept].text
                    attrs=AttribList(adpset.attrib).merge(rept.attrib)
                    setattr(attrs,"period_id",period_id)
                    seg_list.new_file(
                        key, encrypted, attrs
                    )
                    if abs_paths:
                        path = self.abs_path(path)

                    # Not properly supported because we do not know the duration of the
                    # segment. Put 999 for now
                    seg_list.add_file(path, 999, key)
                    from_seg[key] = 0

        return seg_list

    def trim_urls(self):
        """
        Remove all of the hostname parts of any URLs defined in the manifest.
        """
        if self.base_url is not None:
            self.base_url.text = delHTTPhost(self.base_url.text)

        for url in self.urls.values():
            url.text = delHTTPhost(url.text)

    def report_templates(self, dirn=""):
        res = []

        for period, adpset in self.adpsets:
            res += [
                (fn, rband)
                for fn, templ, rband, rid, do_number in self.parse_template(
                    adpset, dirn
                )
            ]

        return res

    def parse_template(self, adpset, dirn="", field="media"):
        """
        Generate a list of files from an Adaption set
        """
        res = []
        if dirn and not dirn.endswith("/"):
            dirn += "/"

        for templ, rband, rid in self.templ[adpset]:
            fn_templ = templ.get(field)
            if fn_templ is None:
                continue

            fn_templ = fn_templ.replace("%", "%%")

            if rband <= 0:
                for rband, rid in self.band_info[adpset]:
                    fn, do_number = self.template_subs(fn_templ, rband, rid)
                    res.append((dirn + fn, templ, rband, rid, do_number))

            else:
                fn, do_number = self.template_subs(fn_templ, rband, rid)
                res.append((dirn + fn, templ, rband, rid, do_number))

        return res

    def template_subs(self, fn_templ, rband, rid, number=None, time=None):
        fn = fn_templ.split("$")
        do_number = False

        for idx in range(1, len(fn), 2):
            sub = fn[idx]

            if sub == "":
                fn[idx] = "$"

            elif sub == "Bandwidth":
                fn[idx] = str(rband)

            elif sub == "RepresentationID":
                fn[idx] = rid

            elif sub.startswith("Time"):
                i = sub.find("%")
                sub = "%d" if i < 0 else sub[i + 1 :]
                fn[idx] = sub % time if time is not None else sub

            elif sub.startswith("Number"):
                do_number = True
                i = sub.find("%")
                sub = "%d" if i < 0 else sub[i + 1 :]
                fn[idx] = sub % number if number is not None else sub

        return "".join(fn), do_number

    PT_parse_regx = re.compile(r"PT([0-9.]+[HMS])([0-9.]+[HMS])?([0-9.]+[HMS])?")

    def check_vod(self):
        """
        Indicate as to whether this is VOD as apposed to live content.
        """
        return self.root.get("type", "").lower() == "static"

    def set_vod(self, state=True):
        """
        Set state of manifest to VOD or live playing.
        """
        self.root.set("type", "static" if state else "dynamic")

    def remove_encryption_tags(self):
        """
        Hide any encryption keys.
        """
        cnt = 0

        for adpset in self.adp_crypt:
            for templ in adpset:
                tag = templ.tag
                if tag.endswith('ContentProtection'):
                    templ.tag = re.sub('ContentProtection','ContentProtection_Transcode_Removed',tag)
                    cnt += 1

        return cnt

    @staticmethod
    def PT_parse_secs(field):
        """
        Convert a PT time duration into seconds
        """
        res = DASHManifest.PT_parse_regx.match(field)
        dur = 0.0

        for grp in res.groups():
            if grp is None:
                break
            if grp.endswith("H"):
                dur += float(grp[:-1]) * 3600.0
            elif grp.endswith("M"):
                dur += float(grp[:-1]) * 60.0
            else:
                dur += float(grp[:-1])

        return dur

    @staticmethod
    def PT_parse(field):
        """
        Convert a PT time duration into seconds
        """
        return timedelta(0, DASHManifest.PT_parse_secs(field))

    @staticmethod
    def PT_format(delta):
        secs = (
            delta.seconds + delta.microseconds / 1000000.0
            if type(delta) is timedelta
            else delta
        )

        if secs > 3600:
            return "PT%dH%dM%dS" % (
                secs // 3600,
                secs % 3600 // 60,
                round(secs % 60, 3),
            )
        if secs > 60:
            return "PT%dM%dS" % (secs % 3600 // 60, round(secs % 60, 3))
        return "PT%.4gS" % round(secs % 60, 3)

