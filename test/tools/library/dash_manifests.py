    def parse_template(self, adpset, dirn="", field="media"):
        """
        Generate a list of files from an Adaption set
        """
        res = []
        if dirn and not dirn.endswith("/"):
            dirn += "/"

        for templ, rband, rid in self.templ[adpset]:
            fn_templ = templ.get(field)
            if fn_templ is None:
                continue

            fn_templ = fn_templ.replace("%", "%%")

            if rband <= 0:
                for rband, rid in self.band_info[adpset]:
                    fn, do_number = self.template_subs(fn_templ, rband, rid)
                    res.append((dirn + fn, templ, rband, rid, do_number))

            else:
                fn, do_number = self.template_subs(fn_templ, rband, rid)
                res.append((dirn + fn, templ, rband, rid, do_number))

        return res

    def template_subs(self, fn_templ, rband, rid, number=None, time=None):
        fn = fn_templ.split("$")
        do_number = False

        for idx in range(1, len(fn), 2):
            sub = fn[idx]

            if sub == "":
                fn[idx] = "$"

            elif sub == "Bandwidth":
                fn[idx] = str(rband)

            elif sub == "RepresentationID":
                fn[idx] = rid

            elif sub.startswith("Time"):
                i = sub.find("%")
                sub = "%d" if i < 0 else sub[i + 1 :]
                fn[idx] = sub % time if time is not None else sub

            elif sub.startswith("Number"):
                do_number = True
                i = sub.find("%")
                sub = "%d" if i < 0 else sub[i + 1 :]
                fn[idx] = sub % number if number is not None else sub

        return "".join(fn), do_number

